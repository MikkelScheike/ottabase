/** Resolve cursor value to CSS cursor declaration. Supports registry:key, svg:..., url(), plain keywords */
export function resolveCursor(value: string, registry: Record<string, string> = CURSOR_SVG_REGISTRY): string {
    if (!value) return 'auto';
    if (!value.includes(':') && !value.includes('(')) return value;

    if (value.startsWith('registry:')) {
        const key = value.slice(9);
        const svg = registry[key];
        if (svg) return svgToCursorUri(svg);
        return 'auto';
    }
    if (value.startsWith('svg:')) return svgToCursorUri(value.slice(4));
    if (value.startsWith('url(')) return value;
    if (value.startsWith('http')) return `url(${value}), auto`;
    return value;
}

function svgToCursorUri(svg: string): string {
    const encoded = encodeURIComponent(svg.trim());
    return `url("data:image/svg+xml;utf8,${encoded}"), auto`;
}
